package BahitJava;

public class test01 {
    public static void main(String[] args) {
        for(int k = 0; k<=7; k++)
            System.out.print("* ");
         /*when use (System.out.print)everything will print in
         * single line just like above */
        System.out.println();

        for(int i = 0; i<6; i++)
            System.out.println("*             *");
        /*when use (System.out.println)everything will print in diffrent line */
        for(int j = 0; j<=7; j++)
            System.out.print("* ");

    }
}
